/**
 * Build öncesi: src/assets/plan/Fertigungsplanung_combined.xlsx dosyasını
 * base64 olarak embedded-combined.generated.ts içine yazar.
 * Böylece publish sonrası combined data her zaman bundle içinde bulunur.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const xlsxPath = path.join(root, 'src', 'assets', 'plan', 'Fertigungsplanung_combined.xlsx');
const outPath = path.join(root, 'src', 'app', 'pages', 'plan', 'embedded-combined.generated.ts');

let base64 = '';
if (fs.existsSync(xlsxPath)) {
  base64 = fs.readFileSync(xlsxPath).toString('base64');
  console.log('embed-combined: Fertigungsplanung_combined.xlsx embedded.');
} else {
  console.warn('embed-combined: Fertigungsplanung_combined.xlsx not found at', xlsxPath, '- using empty.');
}

const content = `/* eslint-disable */
// Auto-generated by scripts/embed-combined.mjs - do not edit manually.
// Combined data is embedded so it is available after publish.

export const EMBEDDED_COMBINED_XLSX_BASE64: string = ${JSON.stringify(base64)};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, content, 'utf8');
